def get_challenge_response(conversation_history: list) -> str:
    response = client.chat.completions.create(
        model=LLAMA_MODEL,
        messages=[{"role": "system", "content": SYSTEM_PROMPT}] + conversation_history,
        temperature=0.7,
        max_tokens=1024,
    )
    return response.choices[0].message.content


def parse_rice_block(text: str) -> dict | None:
    if "---RICE_START---" not in text:
        return None

    start = text.index("---RICE_START---") + len("---RICE_START---")
    end = text.index("---RICE_END---")
    block = text[start:end].strip()

    result = {}
    for line in block.split("\n"):
        if ":" in line:
            key, _, value = line.partition(":")
            result[key.strip()] = value.strip()
    return result
